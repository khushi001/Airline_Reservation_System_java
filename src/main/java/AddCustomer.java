    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String CustomerID=cusID.getText();
        String FirstNmae=FirstName.getText();
        String lastname = LastName.getText();
        String passno = Passno.getText();
        String NatID = NationalID.getText();
        String address = Address.getText();
        String ContactNo = Contact.getText();
        String Gender="";
        if(male.isSelected()) Gender="Male";
        else Gender="Female";
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String Dob=df.format(dob.getDate());
        
        Connection con;
        PreparedStatement pre;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/airline_project","root", "");
            pre=con.prepareStatement("insert into customer(CustomerID,FirstName,LastName,PassportNo,NationalID,Address,Contact,Gender,DateOfBirth) values(?,?,?,?,?,?,?,?,?)");
            pre.setString(1, CustomerID);
            pre.setString(2, FirstNmae);
            pre.setString(3, lastname);
            pre.setString(4, passno);
            pre.setString(5, NatID);
            pre.setString(6, address);
            pre.setString(7, ContactNo);
            pre.setString(8, Gender);
            pre.setString(9, Dob);
            
            pre.executeUpdate();
           
            JOptionPane.showMessageDialog(null, "Added Successfully.");
            
            this.setVisible(false);
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
        

    }//GEN-LAST:event_jButton3ActionPerformed

